export function currentLanguage() {
  return localStorage.getItem("uiLanguage") || "es";
}

export function currentLocale() {
  return currentLanguage() === "pt" ? "pt-BR" : currentLanguage() === "en" ? "en-US" : "es-UY";
}

export function currentTheme() {
  return localStorage.getItem("uiTheme") || "light";
}

export function t(key) {
  const language = currentLanguage();
  return dictionaries[language]?.[key] || dictionaries.es[key] || key;
}

export function translateLiteral(value) {
  const original = String(value ?? "");
  const trimmed = original.trim();
  if (!trimmed) return original;

  const language = currentLanguage();
  const translated = literalDictionaries[language]?.[trimmed] || (language === "es" ? literalDictionaries.es[trimmed] : null);
  if (translated) return original.replace(trimmed, translated);

  const patterned = translatePatterns(trimmed, language);
  return patterned !== trimmed ? original.replace(trimmed, patterned) : original;
}

export function translateElement(root = document.body) {
  if (!root) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.nodeValue?.trim()) return NodeFilter.FILTER_REJECT;
      if (node.parentElement?.closest("script, style")) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });

  const textNodes = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode);
  textNodes.forEach(node => {
    node.nodeValue = translateLiteral(node.nodeValue);
  });

  root.querySelectorAll?.("[placeholder], [title], [aria-label]").forEach(element => {
    ["placeholder", "title", "aria-label"].forEach(attribute => {
      const value = element.getAttribute(attribute);
      if (value) element.setAttribute(attribute, translateLiteral(value));
    });
  });
}

export function translatePage() {
  translateElement(document.body);
}

export function setLanguage(language) {
  localStorage.setItem("uiLanguage", dictionaries[language] ? language : "es");
  applyPreferences();
}

export function setTheme(theme) {
  localStorage.setItem("uiTheme", ["light", "dark", "system"].includes(theme) ? theme : "light");
  applyPreferences();
}

export function applyPreferences() {
  const theme = currentTheme();
  const resolvedTheme = theme === "system" && window.matchMedia?.("(prefers-color-scheme: dark)").matches ? "dark" : theme === "system" ? "light" : theme;
  document.documentElement.dataset.theme = resolvedTheme;
  document.documentElement.dataset.themePreference = theme;
  document.documentElement.lang = currentLocale();
}

export function formatDate(value, options = {}) {
  if (!value) return "-";
  return new Date(value).toLocaleDateString(currentLocale(), options);
}

export function formatDateTime(value, options = {}) {
  if (!value) return "-";
  return new Date(value).toLocaleString(currentLocale(), options);
}

function translatePatterns(value, language) {
  if (language === "en") {
    return value
      .replace(/^Clases de (.+)$/g, "Classes for $1")
      .replace(/^Resetear clave - (.+)$/g, "Reset password - $1")
      .replace(/^Reservar cupo - (.+)$/g, "Reserve seat - $1")
      .replace(/^Alumnos - (.+)$/g, "Students - $1")
      .replace(/^Asistencia de clase - (.+)$/g, "Class attendance - $1")
      .replace(/^No encontre resultados para "(.+)"\.$/g, 'No results found for "$1".')
      .replace(/^Reservar Cancha (\d+)$/g, "Book Court $1")
      .replace(/^Espera (\d+)$/g, "Waiting $1")
      .replace(/\bCancha (\d+)\b/g, "Court $1")
      .replace(/\bJugador (\d+)\b/g, "Player $1")
      .replace(/\b(\d+) libres de (\d+)\b/g, "$1 open of $2")
      .replace(/\bcupo (\d+)\b/g, "capacity $1")
      .replace(/\binvitados: (\d+)\b/g, "guests: $1")
      .replace(/\$ ([\d.,]+) por invitado\b/g, "$$ $1 per guest")
      .replace(/\b(\d+) cupos\b/g, "$1 seats")
      .replace(/\b(\d+) clases\b/g, "$1 classes")
      .replace(/\b(\d+) registros\b/g, "$1 records")
      .replace(/\b(\d+) min\b/g, "$1 min");
  }
  if (language === "pt") {
    return value
      .replace(/^Clases de (.+)$/g, "Aulas de $1")
      .replace(/^Resetear clave - (.+)$/g, "Redefinir senha - $1")
      .replace(/^Reservar cupo - (.+)$/g, "Reservar vaga - $1")
      .replace(/^Alumnos - (.+)$/g, "Alunos - $1")
      .replace(/^Asistencia de clase - (.+)$/g, "Presenca da aula - $1")
      .replace(/^No encontre resultados para "(.+)"\.$/g, 'Nao encontrei resultados para "$1".')
      .replace(/^Reservar Cancha (\d+)$/g, "Reservar Quadra $1")
      .replace(/^Espera (\d+)$/g, "Espera $1")
      .replace(/\bCancha (\d+)\b/g, "Quadra $1")
      .replace(/\bJugador (\d+)\b/g, "Jogador $1")
      .replace(/\b(\d+) libres de (\d+)\b/g, "$1 livres de $2")
      .replace(/\bcupo (\d+)\b/g, "vaga $1")
      .replace(/\binvitados: (\d+)\b/g, "convidados: $1")
      .replace(/\$ ([\d.,]+) por invitado\b/g, "$$ $1 por convidado")
      .replace(/\b(\d+) cupos\b/g, "$1 vagas")
      .replace(/\b(\d+) clases\b/g, "$1 aulas")
      .replace(/\b(\d+) registros\b/g, "$1 registros")
      .replace(/\b(\d+) min\b/g, "$1 min");
  }
  return value
    .replace(/\bActive\b/g, "Activo")
    .replace(/\bInactive\b/g, "Inactivo")
    .replace(/\bWaitingList\b/g, "Lista de espera")
    .replace(/\bOverdue\b/g, "Vencido")
    .replace(/\bConfirmed\b/g, "Confirmado")
    .replace(/\bPending\b/g, "Pendiente");
}
